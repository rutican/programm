#!/bin/bash

# –£–±–µ–¥–∏—Å—å, —á—Ç–æ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ ip_R –∏ pSSH –æ–ø—Ä–µ–¥–µ–ª–µ–Ω—ã –≤ –æ—Å–Ω–æ–≤–Ω–æ–º —Å–∫—Ä–∏–ø—Ç–µ
# (–æ–±—ã—á–Ω–æ –æ–Ω–∏ —ç–∫—Å–ø–æ—Ä—Ç–∏—Ä—É—é—Ç—Å—è –∏–ª–∏ –∑–∞–¥–∞—é—Ç—Å—è –¥–æ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è —ç—Ç–æ–≥–æ —Ñ–∞–π–ª–∞)

vkl_wifi() {
    local BLUE GREEN YELLOW RED NC
    BLUE=$(printf '\033[1;34m')
    GREEN=$(printf '\033[1;32m')
    YELLOW=$(printf '\033[1;33m')
    RED=$(printf '\033[1;31m')
    NC=$(printf '\033[0m')

    local duration

    clear
    printf '%s\n' \
        "" \
        "${BLUE}+------------------------------------------+${NC}" \
        "${BLUE}|${NC}        ${GREEN}–í–∫–ª—é—á–µ–Ω–∏–µ Wi-Fi –Ω–∞ –≤—Ä–µ–º—è${NC}         ${BLUE}|${NC}" \
        "${BLUE}+------------------------------------------+${NC}" \
        ""

    # –¶–∏–∫–ª –≤–≤–æ–¥–∞ —Å –≤–∞–ª–∏–¥–∞—Ü–∏–µ–π
    while true; do
        read -rp "–í–≤–µ–¥–∏—Ç–µ –≤—Ä–µ–º—è –≤ —Ñ–æ—Ä–º–∞—Ç–µ 1‚Äì60m –∏–ª–∏ 1‚Äì100h (–Ω–∞–ø—Ä–∏–º–µ—Ä: 30m, 2h): " duration

        # –ü—Ä–æ–≤–µ—Ä–∫–∞ —Ñ–æ—Ä–º–∞—Ç–∞: –æ—Ç 1 –¥–æ 2 —Ü–∏—Ñ—Ä + 'm' –∏–ª–∏ 'h' –Ω–∞ –∫–æ–Ω—Ü–µ
        if [[ "$duration" =~ ^([1-9][0-9]?)[mh]$ ]]; then
            local num="${BASH_REMATCH[1]}"
            local unit="${duration: -1}"

            # –î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–µ –ø—Ä–æ–≤–µ—Ä–∫–∏ –¥–∏–∞–ø–∞–∑–æ–Ω–∞
            if [[ "$unit" == "m" && "$num" -ge 1 && "$num" -le 60 ]] || \
               [[ "$unit" == "h" && "$num" -ge 1 && "$num" -le 100 ]]; then
                break
            fi
        fi

        echo -e "\n${RED}‚ùó –ù–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ. –î–æ–ø—É—Å—Ç–∏–º–æ: 1‚Äì60m –∏–ª–∏ 1‚Äì100h.${NC}\n"
    done

    echo -e "\nüì° –û—Ç–ø—Ä–∞–≤–∫–∞ –∫–æ–º–∞–Ω–¥—ã –Ω–∞ —Ä–æ—É—Ç–µ—Ä ($ip_R:$pSSH)..."

    # –í—ã–ø–æ–ª–Ω—è–µ–º SSH-–∫–æ–º–∞–Ω–¥—É
    if ssh "router_user@$ip_R" -p "$pSSH" "[:parse [/system script get wifi_on source ]] ztime=$duration" 2>/dev/null; then
        echo -e "\n‚úÖ Wi-Fi –±—É–¥–µ—Ç –≤–∫–ª—é—á—ë–Ω."
        echo -e "\nüïí –¢–µ–∫—É—â–µ–µ –≤—Ä–µ–º—è: $(date '+%H:%M:%S %d-%m-%Y')"
        echo -e "\n‚è≥ Wi-Fi –≤—ã–∫–ª—é—á–∏—Ç—Å—è –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ —á–µ—Ä–µ–∑: $duration"
    else
        echo -e "\n‚ùå –û—à–∏–±–∫–∞ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è –∫ —Ä–æ—É—Ç–µ—Ä—É. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ —Å–µ—Ç—å, SSH –∏ —É—á—ë—Ç–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ."
    fi

    echo -e "\n–ù–∞–∂–º–∏—Ç–µ Enter –¥–ª—è –≤–æ–∑–≤—Ä–∞—Ç–∞..."
    read -r
}
